-- Enable RLS on all tables that need it
ALTER TABLE public.inventory ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.products ENABLE ROW LEVEL SECURITY;

-- Create policies for inventory
CREATE POLICY IF NOT EXISTS "Allow all operations on inventory" 
ON public.inventory 
FOR ALL 
USING (true) 
WITH CHECK (true);

-- Create policies for products
CREATE POLICY IF NOT EXISTS "Allow all operations on products" 
ON public.products 
FOR ALL 
USING (true) 
WITH CHECK (true);

-- Fix function search paths for all update timestamp functions
CREATE OR REPLACE FUNCTION public.update_timestamp_column()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$;

